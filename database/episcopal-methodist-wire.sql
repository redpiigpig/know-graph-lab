-- ============================================================
-- 衛理宗使徒統緒接點：把美國衛理會根座接回聖公會母會
-- ============================================================
-- 背景：全球衛理公會（UMC / AME Zion / CME / GMC / 南非 / 印度 / 韓國 / 自由衛理 /
-- 台灣 中華基督教衛理公會 …）的 episcopal_sees 都已建好，且全部 parent_see_id 指向
-- 「巴爾的摩（衛理）」這個美國根座（1784 巴爾的摩聖誕大會 = 美國衛理會主教制起點）。
-- 但根座本身 parent_see_id = NULL → 整支衛理宗在族譜圖上孤立、接不到使徒統緒。
--
-- 修正：把根座接到「倫敦｜英格蘭教會」。理由——
--   • 約翰·衛斯理 1728 由聖公會（牛津主教約翰·波特，後任坎特伯里大主教）按立為長老；
--   • 18 世紀倫敦主教（時任愛德蒙·吉布森 1723-1748）兼轄北美殖民地聖公會，
--     北美無主教正是 1784 衛斯理自行按立科克/亞斯理的歷史背景。
--   • 屬「非正規的衛斯理統緒」（長老按立主教，聖公會/天主教不承認其觸手統緒）——
--     記於根座 notes，連接線走一般 spine 色實線（不同 see_zh → 設立式，非紅鋸齒分裂）。
--
-- 接好後鏈路暢通：
--   羅馬教宗 → 坎特伯里(額我略一世→奧古斯丁 597) → 坎特伯里英格蘭教會(克蘭默 1533)
--   → 倫敦英格蘭教會 → 巴爾的摩衛理(衛斯理→科克→亞斯理) → …全球各會… → 台北衛理(蕢建華…龐君華…辛俊傑)
--
-- 執行方式：Supabase Management API（已於 2026-06-02 套用至 production DB）。
-- ============================================================

UPDATE episcopal_sees
SET parent_see_id = (SELECT id FROM episcopal_sees WHERE see_zh = '倫敦' AND church = '英格蘭教會' LIMIT 1),
    tradition       = '基督新教',
    founder_apostle_id = NULL,   -- 衛理宗非使徒源頭座，務必為 NULL（否則 BFS 會跳過）
    split_year      = NULL,      -- 設立式（不同 see_zh）；保留 1738 前的衛斯理兄弟不被 split 過濾掉
    notes = COALESCE(notes, '') || ' ｜ 使徒統緒接點：循衛斯理 1728 受倫敦/牛津聖公會按立為長老之緒；'
            || '1784 衛斯理以長老身分自任主教權柄按立科克、亞斯理，開美國衛理會主教制——'
            || '此為非正規（非主教按立主教）的衛斯理統緒，與聖公會母會在倫敦主教（時任愛德蒙·吉布森，兼轄北美殖民地聖公會）一線相承。'
WHERE see_zh = '巴爾的摩（衛理）' AND church = '衛理宗';
