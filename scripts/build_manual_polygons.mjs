// ===== Build =====
function signedArea(ring) {
  let a = 0
  for (let i = 0; i < ring.length - 1; i++) {
    a += (ring[i + 1][0] - ring[i][0]) * (ring[i + 1][1] + ring[i][1])
  }
  return a / 2
}

// 我手繪頂點是地理 CCW（北→東→南→西→北），shoelace 正值 = CW（d3-geo 球面要的）
// 但我頂點順序是 CCW；shoelace 應為負；reverse 變正 CW
function ensureCW(ring) {
  const a = signedArea(ring)
  // a > 0 = CW in my shoelace formula = correct for d3-geo
  return a > 0 ? ring : [...ring].reverse()
}

const features = []
for (const emp of EMPIRES) {
  const sorted = [...emp.keyframes].sort((a, b) => a.year - b.year)
  for (let i = 0; i < sorted.length; i++) {
    const kf = sorted[i]
    const nextYear = sorted[i + 1]?.year ?? null
    const year_to = kf.year_to ?? (nextYear !== null ? nextYear - 1 : kf.year)

    // Process rings
    const rings = kf.rings.map((r) => {
      // Ensure closed
      const closed = r[0][0] === r[r.length - 1][0] && r[0][1] === r[r.length - 1][1]
        ? r
        : [...r, r[0]]
      return ensureCW(closed)
    })

    let geom
    if (rings.length === 1) {
      geom = { type: 'Polygon', coordinates: rings }
    } else {
      geom = { type: 'MultiPolygon', coordinates: rings.map((r) => [r]) }
    }

    features.push({
      type: 'Feature',
      geometry: geom,
      properties: {
        name: emp.polygon_name,
        name_zh: emp.name_zh,
        year_from: kf.year,
        year_to,
        label: kf.label,
        source: 'manual',
      },
    })
  }
  console.log(`[${emp.polygon_name}] ${sorted.length} keyframes`)
}

const out = { type: 'FeatureCollection', features }
writeFileSync(OUT, JSON.stringify(out))
const size = Buffer.byteLength(JSON.stringify(out)) / 1024
console.log(`\n✅ ${features.length} polygons / ${EMPIRES.length} empires → ${OUT}`)
console.log(`   ${size.toFixed(1)} KB`)
