def normalize(t: str) -> str:
    """Normalize a title for fuzzy matching."""
    t = t.lower().strip()
    t = re.sub(r"\.pdf$", "", t)
    t = re.sub(r"[（(].*?[）)]", "", t)  # remove (上)(下)(2022)(英) etc.
    t = re.sub(r"[【\[].*?[】\]]", "", t)  # remove 【…】[…]
    t = re.sub(r"\s+", " ", t).strip()
    return t


