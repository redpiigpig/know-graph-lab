// 加欄位
console.log('Adding sort_order column...')
await sql(`ALTER TABLE biblical_people ADD COLUMN IF NOT EXISTS sort_order INT`)

// 每次 50 人批次 UPDATE
const BATCH = 50
for (let i = 0; i < ORDER.length; i += BATCH) {
  const slice = ORDER.slice(i, i + BATCH)
  const cases = slice.map((name, j) => `WHEN '${name.replace(/'/g, "''")}' THEN ${i + j + 1}`).join('\n    ')
  const names = slice.map(n => `'${n.replace(/'/g, "''")}'`).join(',')
  const query = `
    UPDATE biblical_people
    SET sort_order = CASE name_zh
      ${cases}
    END
    WHERE name_zh IN (${names})
  `
  const r = await sql(query)
  console.log(`Batch ${i}-${i + slice.length}: status ${r.status}`)
}

// 沒有對應名稱的人排到最後
await sql(`UPDATE biblical_people SET sort_order = 9999 WHERE sort_order IS NULL`)
