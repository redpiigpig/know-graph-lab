async function patchOne({ source_url, update }) {
  const { data: existing, error: findErr } = await supabase
    .from('pong_reports')
    .select('id, title, published_at, author, content')
    .eq('source_url', source_url)
    .single()

  if (findErr || !existing) {
    console.error(`❌ 找不到記錄 [${source_url.slice(0, 50)}…]：`, findErr?.message)
    return
  }

  console.log(`\n找到 [id=${existing.id}]：${existing.title.slice(0, 40)}`)
  console.log(`  published_at: ${existing.published_at}  author: ${existing.author}  content: ${existing.content ? '有' : '無'}`)

  const { error: updateErr } = await supabase
    .from('pong_reports')
    .update(update)
    .eq('id', existing.id)

  if (updateErr) {
    console.error(`❌ 更新失敗：`, updateErr.message)
    return
  }

  const { data: after } = await supabase
    .from('pong_reports')
    .select('id, title, author, published_at, content')
    .eq('id', existing.id)
    .single()

  console.log(`✅ 更新完成`)
  console.log(`  author: ${after.author}  published_at: ${after.published_at}  content 長度: ${after.content?.length} 字元`)
}

