async function insertOne(article) {
  const { data: existing } = await supabase
    .from('pong_reports')
    .select('id')
    .eq('source_url', article.source_url)
    .maybeSingle()

  if (existing) {
    console.log(`\n⏭  已存在，跳過：${article.title.slice(0, 40)}`)
    return
  }

  const { error } = await supabase.from('pong_reports').insert(article)
  if (error) {
    console.error(`\n❌ 新增失敗：${article.title.slice(0, 40)}`, error.message)
  } else {
    console.log(`\n✅ 新增完成：${article.title.slice(0, 40)}`)
  }
}

