async function main() {
  for (const { id, update } of PATCHES) {
    const { error } = await supabase.from('pong_writings').update(update).eq('id', id)
    if (error) {
      console.error(`❌ 更新 id=${id} 失敗：`, error.message)
    } else {
      console.log(`✅ 更新 id=${id} 完成`)
    }
  }

  for (const article of INSERTS) {
    const { data: existing } = await supabase
      .from('pong_writings')
      .select('id')
      .eq('title', article.title)
      .maybeSingle()

    if (existing) {
      console.log(`⏭  已存在，跳過：${article.title}`)
      continue
    }

    const { error } = await supabase.from('pong_writings').insert(article)
    if (error) {
      console.error(`❌ 新增失敗：${article.title}`, error.message)
    } else {
      console.log(`✅ 新增完成：${article.title}`)
    }
  }
}

main().catch(err => { console.error(err); process.exit(1) })
