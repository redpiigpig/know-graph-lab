首先，感謝大學宗教系主辦本次的活動，當我和教會的會友提起要參加一個由天主教大學所主辦的紀念宗教改革運動500週年的活動（之前在今年三月也有聖伯敏神學院所主辦同一紀念的座談）時，他們開始都會感到好奇，因為在我們一般印象中，好像應該是改革陣營（新教）的教會才會來慶祝或紀念宗教改革才對。

