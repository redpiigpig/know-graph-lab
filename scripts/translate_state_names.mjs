for (const s of states) {
  if (s.name_zh) continue
  // 1) Manual
  if (MANUAL_TRANSLATIONS[s.name_en]) {
    s.name_zh = MANUAL_TRANSLATIONS[s.name_en]
    filledManual += 1
    continue
  }
  // 2) Rule
  const r = ruleTranslate(s.name_en)
  if (r) {
    s.name_zh = r
    filledRule += 1
  }
}

