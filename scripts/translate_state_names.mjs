function ruleTranslate(name) {
  // Prefix 模式
  for (const [prefix, { suffix }] of Object.entries(PREFIX_DICT)) {
    if (name.startsWith(prefix)) {
      const place = name.slice(prefix.length).trim()
      // 地名本身若沒翻譯就保留英文（這時整體還是有「X 王國」格式，比 null 好）
      return place + ' ' + suffix
    }
  }
  // Suffix 模式
  for (const [suffix, zhSuffix] of Object.entries(SUFFIX_DICT)) {
    if (name.endsWith(suffix)) {
      const stem = name.slice(0, name.length - suffix.length).trim()
      return stem + zhSuffix
    }
  }
  return null
}

