const activeSession = ref<number>(59);
const search = ref('');

const active = computed(() => sessions.find(s => s.number === activeSession.value));

const filteredCommittees = computed(() => {
  const q = search.value.trim();
  if (!q || !active.value) return active.value?.committees ?? [];
  return active.value.committees.filter(c =>
    c.rosters.some(r => r.names.includes(q))
  );
});

const escapeHtml = (s: string) => s
  .replace(/&/g, '&amp;')
  .replace(/</g, '&lt;')
  .replace(/>/g, '&gt;');

const escapeRegExp = (s: string) => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

const highlight = (text: string) => {
  const safe = escapeHtml(text);
  const q = search.value.trim();
  if (!q) return safe;
  const re = new RegExp(escapeRegExp(escapeHtml(q)), 'g');
  return safe.replace(re, '<mark class="bg-indigo-100 text-indigo-900 rounded px-0.5">$&</mark>');
};
