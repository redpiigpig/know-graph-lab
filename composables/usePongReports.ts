export const usePongReports = () => {
  const flatReports = sessions.flatMap(s =>
    s.reports.map(r => ({
      ...r,
      sessionNumber: s.number,
      sessionYear: s.year,
      sessionMeetingTitle: s.meetingTitle,
    }))
  );
  const findById = (id: string) => flatReports.find(r => r.id === id) ?? null;
  return { sessions, flatReports, findById };
};
