//   經‧律‧論‧宣‧函‧儀‧詩‧譯‧史‧類（顯示順序由 index.ts 的 CANON_ORDER 統一）。
//
// 三軌定義（見 types.ts CANON_LABEL，奠基於「隱密的上帝」與「社會學邊界」）：
//   正藏 = 基督教群體教會論／社會學邊界「之內」的家族記憶（非啟示壟斷）。
//   外藏 = 邊界「之外」、隱密上帝按其自由開展的平行啟示與神聖見證；「外」無貶義。
//
// ⚠️ 鐵則（所有研究 agent 與人工皆須遵守）：
//   1. 跨藏去重：除經藏可作核心並允許與他藏重複外，同一書卷不得同時出現在兩藏。
//   2. 定名對齊 /translation-glossary（金口若望、大良、大額我略、猶斯定、革利免 vs 克勉…）。
//   3. 託名 not 偽（託名狄奧尼修斯／託名馬太…；「偽典／偽托」為文類詞可留）。
//   4. 每卷標 author(或傳說作者)／era(寫作日期)／place(寫作地點)／language(語言) + 100–200 字 intro。
//   5. 外藏必收「與基督教相關、對話、衝突」的異端、外教、猶太教、世俗文獻（見各藏 waiScope）。
//   6. 候選須附出處（來源全集／頁碼／URL），供人工過目查核，杜絕幻覺書目。
//   7. 正典封閉法則：使徒時代後正典封閉，唯【古代】有【正經藏】；中／近／今三代經藏
